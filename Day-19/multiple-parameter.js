// addition
function addition(number1, number2) {
  var total = number1 + number2;
  return total;
}

var additionResult = addition(19, 35);
console.log("Total of 19 & 35 is " + additionResult);

// substractio
function substraction(number1, nuber2) {
  var substractionResult = number1 - nuber2;
  return substractionResult;
}

var subsresult = substraction(65, 17);
console.log("The result of substraction is " + subsresult);

// multiplication

function multiplication(number1, number2) {
  var total = number1 * number2;
  return total;
}

var multiResult = multiplication(27, 37);
console.log("The result of multiplication is " + multiResult);

